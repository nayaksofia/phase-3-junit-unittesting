package com.ecommerce.webapp;

public class CalculatorApp {

	//Addition
	public int add(int a, int b) {
		return a+b;
	}
	
	//Subtraction
	public int sub(int a, int b) {
		return a-b;
	}
	
	//Multplication
	public int mul(int a, int b) {
		return a*b;
	}
	
	//Division
	public int div(int a, int b) {
		return a/b;
	}
	
}
